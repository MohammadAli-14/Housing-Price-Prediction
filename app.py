@app.route('/predict', methods=['POST'])
def predict():
    """API endpoint for making predictions."""
    try:
        data = request.get_json()
        
        # Extract features
        features = {
            'MedInc': float(data.get('MedInc', 0)),
            'HouseAge': float(data.get('HouseAge', 0)),
            'AveRooms': float(data.get('AveRooms', 0)),
            'AveBedrms': float(data.get('AveBedrms', 0)),
            'Population': float(data.get('Population', 0)),
            'AveOccup': float(data.get('AveOccup', 0)),
            'Latitude': float(data.get('Latitude', 0)),
            'Longitude': float(data.get('Longitude', 0)),
            'Households': float(data.get('Households', 0))
        }
        
        # Make prediction
        prediction = predict_price(features)
        
        return jsonify({
            'success': True,
            'prediction': float(prediction),
            'features': features
        })
        
    except Exception as e:
        return jsonify({
            'success': False,
            'error': str(e)
        })

