MODEL_PATH = 'best_model_gradient_boosting_(tuned).joblib'
METRICS_PATH = 'model_metrics.json'

# Global variables for model components
model_dict = None
model = None
scaler = None
feature_selector = None
selected_features = None
model_metrics = None

def load_model():
    """Load the trained model and metrics."""
    global model_dict, model, scaler, feature_selector, selected_features, model_metrics
    
    if os.path.exists(MODEL_PATH):
        model_dict = joblib.load(MODEL_PATH)
        
        # Extract components from the dictionary
        if isinstance(model_dict, dict):
            model = model_dict.get('model')
            scaler = model_dict.get('scaler')
            feature_selector = model_dict.get('feature_selector')
            selected_features = model_dict.get('selected_features')
            print(f"✓ Model components loaded from {MODEL_PATH}")
            print(f"  - Model: {type(model).__name__}")
            print(f"  - Scaler: {type(scaler).__name__ if scaler else 'None'}")
            print(f"  - Feature Selector: {type(feature_selector).__name__ if feature_selector else 'None'}")
            print(f"  - Selected Features: {len(selected_features) if selected_features else 0} features")
        else:
            # If it's already a model, use it directly
            model = model_dict
            print(f"✓ Model loaded from {MODEL_PATH}")
    else:
        raise FileNotFoundError(f"Model file not found: {MODEL_PATH}")
    
    if os.path.exists(METRICS_PATH):
        with open(METRICS_PATH, 'r') as f:
            model_metrics = json.load(f)
        print(f"✓ Metrics loaded from {METRICS_PATH}")
    
    return model

def engineer_features(df):
    """
    Apply the same feature engineering as during training.
    Must create ALL 16 features that the scaler was fitted on.
    
    Expected input features:
    - MedInc: Median income in block group
    - HouseAge: Median house age in block group
    - AveRooms: Average number of rooms per household
    - AveBedrms: Average number of bedrooms per household
    - Population: Block group population
    - AveOccup: Average number of household members
    - Latitude: Block group latitude
    - Longitude: Block group longitude
    - Households: Number of households in block group
    """
    df = df.copy()
    
    # Feature Engineering (matching training preprocessing)
    # 1. Income per Room
    df['IncomePerRoom'] = df['MedInc'] / (df['AveRooms'] + 0.001)
    
    # 2. Location Interaction
    df['LatLongInteraction'] = df['Latitude'] * df['Longitude']
    
    # 3. Income Polynomials
    df['MedInc_Squared'] = df['MedInc'] ** 2
    df['MedInc_Cubed'] = df['MedInc'] ** 3
    
    # 4. Age Groups (All three for 16 features total)
    df['AgeGroup_Young'] = (df['HouseAge'] < 20).astype(int)
    df['AgeGroup_Medium'] = ((df['HouseAge'] >= 20) & (df['HouseAge'] < 40)).astype(int)
    df['AgeGroup_Old'] = (df['HouseAge'] >= 40).astype(int)
    
    # All 16 features in the order the scaler expects
    feature_columns = [
        'MedInc', 'HouseAge', 'AveRooms', 'AveBedrms', 'Population', 'AveOccup',
        'Latitude', 'Longitude', 'Households', 'IncomePerRoom', 'LatLongInteraction',
        'MedInc_Squared', 'MedInc_Cubed', 'AgeGroup_Young', 'AgeGroup_Medium', 'AgeGroup_Old'
    ]
    
    return df[feature_columns]

def predict_price(features_dict):
    """
    Make a prediction using the loaded model.
    
    Args:
        features_dict: Dictionary with input features
        
    Returns:
        Predicted house price
    """
    global model, scaler, feature_selector, selected_features
    
    if model is None:
        load_model()
    
    # Create DataFrame from input
    df = pd.DataFrame([features_dict])
    
    # Apply feature engineering
    df_engineered = engineer_features(df)
    
    # Get the feature values as numpy array
    X = df_engineered.values
    
    # Apply scaling if scaler exists
    if scaler is not None:
        X = scaler.transform(X)
    
    # Apply feature selection if selector exists
    if feature_selector is not None:
        X = feature_selector.transform(X)
    
    # Make prediction
    prediction = model.predict(X)
    
    return prediction[0]

